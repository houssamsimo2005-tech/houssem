class BudgetViewModel(application: Application) : AndroidViewModel(application) {

    private val repository: ProjectRepository
    val allProjects: StateFlow<List<Project>>

    // The currently active project
    val currentProject = MutableStateFlow<Project?>(null)

    // The working project data (being modified in the UI)
    val currentData = MutableStateFlow(ProjectData.createDefault())

    init {
        val database = AppDatabase.getDatabase(application)
        repository = ProjectRepository(database.projectDao())
        allProjects = repository.allProjects.stateIn(
            scope = viewModelScope,
            started = SharingStarted.WhileSubscribed(5000),
            initialValue = emptyList()
        )
    }

    // Set the current working project
    fun selectProject(project: Project) {
        currentProject.value = project
        currentData.value = project.data
    }

    // Unselect project and load default working data
    fun createNewProjectSession() {
        currentProject.value = null
        currentData.value = ProjectData.createDefault()
    }

    // Auto-generate 12 months based on custom parameters
    fun autoGenerateData(
        baseSalesQty: Double,
        growthPercent: Double,
        price: Double,
        rmCost: Double,
        laborRateVal: Double,
        varOverhead: Double,
        fixedOH: Double,
        fixedSA: Double,
        depr: Double,
        initCashVal: Double
    ) {
        currentData.value = ProjectData.createDefault(
            baseSalesQty = baseSalesQty,
            growthPercent = growthPercent,
            price = price,
            rmCost = rmCost,
            laborRateVal = laborRateVal,
            varOverhead = varOverhead,
            fixedOH = fixedOH,
            fixedSA = fixedSA,
            depr = depr,
            initCashVal = initCashVal
        )
    }

    // Save project (either insert new or update existing)
    fun saveProject(projectName: String, onSuccess: () -> Unit) {
        viewModelScope.launch {
            val currentProj = currentProject.value
            if (currentProj != null) {
                // Update
                val updated = currentProj.copy(name = projectName, data = currentData.value)
                repository.update(updated)
                currentProject.value = updated
            } else {
                // Insert
                val newProj = Project(name = projectName, data = currentData.value)
                val id = repository.insert(newProj)
                currentProject.value = newProj.copy(id = id.toInt())
            }
            onSuccess()
        }
    }

    // Delete a project
    fun deleteProject(projectId: Int) {
        viewModelScope.launch {
            if (currentProject.value?.id == projectId) {
                currentProject.value = null
            }
            repository.deleteById(projectId)
        }
    }

    // Update specific inputs in working data
    fun updateWorkingData(updated: ProjectData) {
        currentData.value = updated
    }

