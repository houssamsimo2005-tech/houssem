    // Direct HTML-based Printing & PDF Generation
    fun printReport(context: Context, engine: BudgetEngine, title: String, contentHtml: String) {
        val webView = WebView(context)
        webView.webViewClient = object : WebViewClient() {
            override fun onPageFinished(view: WebView?, url: String?) {
                val printManager = context.getSystemService(Context.PRINT_SERVICE) as PrintManager
                val printAdapter = webView.createPrintDocumentAdapter(title)
                val jobName = "Houssem Compta - $title"
                printManager.print(jobName, printAdapter, PrintAttributes.Builder().build())
            }
        }
        webView.loadDataWithBaseURL(null, contentHtml, "text/html", "utf-8", null)
    }

